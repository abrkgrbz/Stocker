'use client';

import React, { useState } from 'react';
import {
  Card,
  Button,
  Input,
  Space,
  Tag,
  Typography,
  Row,
  Col,
  Modal,
  message,
  Avatar,
  Tooltip,
} from 'antd';
import {
  PlusOutlined,
  SearchOutlined,
  DollarOutlined,
  TrophyOutlined,
  CloseCircleOutlined,
  ReloadOutlined,
  AppstoreOutlined,
  UnorderedListOutlined,
  ArrowRightOutlined,
  CheckCircleOutlined,
  StopOutlined,
} from '@ant-design/icons';
import type { Deal } from '@/lib/api/services/crm.service';
import {
  useDeals,
  useCreateDeal,
  useUpdateDeal,
  useDeleteDeal,
  useMoveDealStage,
  useCloseDealWon,
  useCloseDealLost,
} from '@/lib/api/hooks/useCRM';
import { DealsStats } from '@/components/crm/deals/DealsStats';
import { DealModal } from '@/features/deals/components';
import dayjs from 'dayjs';

const { Title, Text } = Typography;

// Pipeline stages
const stages = [
  { id: 1, name: 'Yeni', color: '#1890ff' },
  { id: 2, name: 'Ä°letiÅŸim', color: '#13c2c2' },
  { id: 3, name: 'Teklif', color: '#52c41a' },
  { id: 4, name: 'MÃ¼zakere', color: '#faad14' },
  { id: 5, name: 'KapalÄ±', color: '#722ed1' },
];

// Status colors
const statusColors: Record<Deal['status'], string> = {
  Open: 'blue',
  Won: 'green',
  Lost: 'red',
};

export default function DealsPage() {
  const [searchText, setSearchText] = useState('');
  const [viewMode, setViewMode] = useState<'kanban' | 'list'>('kanban');
  const [modalOpen, setModalOpen] = useState(false);
  const [selectedDeal, setSelectedDeal] = useState<Deal | null>(null);

  // API Hooks
  const { data, isLoading, refetch } = useDeals({});
  const createDeal = useCreateDeal();
  const updateDeal = useUpdateDeal();
  const deleteDeal = useDeleteDeal();
  const moveDealStage = useMoveDealStage();
  const closeDealWon = useCloseDealWon();
  const closeDealLost = useCloseDealLost();

  const deals = data?.items || [];

  // Calculate statistics
  const stats = {
    total: deals.filter((d) => d.status === 'Open').length,
    totalAmount: deals.filter((d) => d.status === 'Open').reduce((sum, d) => sum + d.amount, 0),
    won: deals.filter((d) => d.status === 'Won').length,
    wonAmount: deals.filter((d) => d.status === 'Won').reduce((sum, d) => sum + d.amount, 0),
  };

  const handleCreate = () => {
    setSelectedDeal(null);
    setModalOpen(true);
  };

  const handleEdit = (deal: Deal) => {
    setSelectedDeal(deal);
    setModalOpen(true);
  };

  const handleDelete = async (id: number) => {
    Modal.confirm({
      title: 'FÄ±rsatÄ± Sil',
      content: 'Bu fÄ±rsatÄ± silmek istediÄŸinizden emin misiniz?',
      okText: 'Sil',
      okType: 'danger',
      cancelText: 'Ä°ptal',
      onOk: async () => {
        try {
          await deleteDeal.mutateAsync(id);
          message.success('FÄ±rsat silindi');
        } catch (error) {
          message.error('Silme iÅŸlemi baÅŸarÄ±sÄ±z');
        }
      },
    });
  };

  const handleMoveStage = async (dealId: number, newStageId: number) => {
    try {
      await moveDealStage.mutateAsync({
        id: dealId.toString(),
        newStageId: newStageId.toString(),
      });
      message.success('FÄ±rsat aÅŸamasÄ± deÄŸiÅŸtirildi');
    } catch (error: any) {
      const apiError = error.response?.data;
      const errorMessage = apiError?.detail || apiError?.errors?.[0]?.message || apiError?.title || error.message || 'AÅŸama deÄŸiÅŸtirme baÅŸarÄ±sÄ±z';
      message.error(errorMessage);
    }
  };

  const handleCloseWon = async (deal: Deal) => {
    Modal.confirm({
      title: 'FÄ±rsatÄ± KazanÄ±ldÄ± Olarak Ä°ÅŸaretle',
      content: `"${deal.title}" fÄ±rsatÄ±nÄ± kazanÄ±ldÄ± olarak iÅŸaretlemek istediÄŸinizden emin misiniz?`,
      okText: 'KazanÄ±ldÄ± Ä°ÅŸaretle',
      okType: 'primary',
      cancelText: 'Ä°ptal',
      onOk: async () => {
        try {
          await closeDealWon.mutateAsync({
            id: deal.id.toString(),
            actualAmount: deal.amount,
            closedDate: new Date().toISOString(),
          });
          message.success('ðŸŽ‰ FÄ±rsat kazanÄ±ldÄ± olarak iÅŸaretlendi!');
        } catch (error: any) {
          const apiError = error.response?.data;
          const errorMessage = apiError?.detail || apiError?.errors?.[0]?.message || apiError?.title || error.message || 'Ä°ÅŸlem baÅŸarÄ±sÄ±z';
          message.error(errorMessage);
        }
      },
    });
  };

  const handleCloseLost = async (deal: Deal) => {
    Modal.confirm({
      title: 'FÄ±rsatÄ± Kaybedildi Olarak Ä°ÅŸaretle',
      content: `"${deal.title}" fÄ±rsatÄ±nÄ± kaybedildi olarak iÅŸaretlemek istediÄŸinizden emin misiniz?`,
      okText: 'Kaybedildi Ä°ÅŸaretle',
      okType: 'danger',
      cancelText: 'Ä°ptal',
      onOk: async () => {
        try {
          await closeDealLost.mutateAsync({
            id: deal.id.toString(),
            lostReason: 'KullanÄ±cÄ± tarafÄ±ndan kapatÄ±ldÄ±',
            closedDate: new Date().toISOString(),
          });
          message.info('FÄ±rsat kaybedildi olarak iÅŸaretlendi');
        } catch (error: any) {
          const apiError = error.response?.data;
          const errorMessage = apiError?.detail || apiError?.errors?.[0]?.message || apiError?.title || error.message || 'Ä°ÅŸlem baÅŸarÄ±sÄ±z';
          message.error(errorMessage);
        }
      },
    });
  };

  const handleSubmit = async (values: any) => {
    try {
      // Validation: CustomerId is required by backend
      if (!values.customerId) {
        message.error('MÃ¼ÅŸteri seÃ§imi zorunludur');
        return;
      }

      // Validation: ExpectedCloseDate is required and must be in future
      if (!values.expectedCloseDate) {
        message.error('Tahmini kapanÄ±ÅŸ tarihi zorunludur');
        return;
      }

      const dealData: any = {
        title: values.title,
        customerId: values.customerId,
        amount: values.amount,
        probability: values.probability || 50,
        expectedCloseDate: values.expectedCloseDate.toISOString(),
        status: values.status || 'Open',
        priority: values.priority || 'Medium',
      };

      // Add optional fields only if they have values (avoid sending undefined)
      if (values.description) dealData.description = values.description;
      if (values.pipelineId) dealData.pipelineId = values.pipelineId;
      if (values.stageId) dealData.currentStageId = values.stageId;

      if (selectedDeal) {
        await updateDeal.mutateAsync({ id: selectedDeal.id, data: dealData });
        message.success('FÄ±rsat gÃ¼ncellendi');
      } else {
        await createDeal.mutateAsync(dealData);
        message.success('FÄ±rsat oluÅŸturuldu');
      }
      setModalOpen(false);
    } catch (error: any) {
      // Extract API error details
      const apiError = error.response?.data;
      let errorMessage = 'Ä°ÅŸlem baÅŸarÄ±sÄ±z';

      if (apiError) {
        errorMessage = apiError.detail ||
                      apiError.errors?.[0]?.message ||
                      apiError.title ||
                      errorMessage;
      } else if (error.message) {
        errorMessage = error.message;
      }

      message.error(errorMessage);
    }
  };

  const handleDragEnd = async (dealId: number, newStageId: number) => {
    try {
      await updateDeal.mutateAsync({ id: dealId, data: { stageId: newStageId } });
      message.success('FÄ±rsat aÅŸamasÄ± gÃ¼ncellendi');
    } catch (error: any) {
      // Extract API error details
      const apiError = error.response?.data;
      let errorMessage = 'GÃ¼ncelleme baÅŸarÄ±sÄ±z';

      if (apiError) {
        errorMessage = apiError.detail ||
                      apiError.errors?.[0]?.message ||
                      apiError.title ||
                      errorMessage;
      } else if (error.message) {
        errorMessage = error.message;
      }

      message.error(errorMessage);
    }
  };

  // Filter deals based on search
  const filteredDeals = deals.filter((deal) => {
    const searchLower = searchText.toLowerCase();
    return deal.title.toLowerCase().includes(searchLower) || deal.description?.toLowerCase().includes(searchLower) || '';
  });

  // Group deals by stage for Kanban view
  const dealsByStage = stages.reduce(
    (acc, stage) => {
      acc[stage.id] = filteredDeals.filter((d) => d.stageId === stage.id && d.status === 'Open');
      return acc;
    },
    {} as Record<number, Deal[]>
  );

  // Deal Card Component
  const DealCard = ({ deal }: { deal: Deal }) => (
    <Card
      className="mb-3 hover:shadow-md transition-shadow"
      bodyStyle={{ padding: '12px' }}
    >
      <div className="flex justify-between items-start mb-2">
        <Text strong className="text-sm cursor-pointer" onClick={() => handleEdit(deal)}>
          {deal.title}
        </Text>
        <Tag color={statusColors[deal.status]}>{deal.status}</Tag>
      </div>

      <div className="text-lg font-semibold text-green-600 mb-2">
        â‚º{deal.amount.toLocaleString('tr-TR')}
      </div>

      <div className="flex items-center justify-between text-xs text-gray-500 mb-3">
        <Tooltip title="OlasÄ±lÄ±k">
          <span>{deal.probability}%</span>
        </Tooltip>
        {deal.expectedCloseDate && (
          <Tooltip title="Tahmini KapanÄ±ÅŸ">
            <span>{dayjs(deal.expectedCloseDate).format('DD/MM/YYYY')}</span>
          </Tooltip>
        )}
      </div>

      {/* Action Buttons - Only show for Open deals */}
      {deal.status === 'Open' && (
        <Space size="small" className="w-full" direction="vertical">
          <Space size="small" className="w-full">
            <Tooltip title="KazanÄ±ldÄ±">
              <Button
                type="primary"
                size="small"
                icon={<CheckCircleOutlined />}
                onClick={(e) => {
                  e.stopPropagation();
                  handleCloseWon(deal);
                }}
                block
              >
                KazanÄ±ldÄ±
              </Button>
            </Tooltip>
            <Tooltip title="Kaybedildi">
              <Button
                danger
                size="small"
                icon={<StopOutlined />}
                onClick={(e) => {
                  e.stopPropagation();
                  handleCloseLost(deal);
                }}
                block
              >
                Kaybedildi
              </Button>
            </Tooltip>
          </Space>
        </Space>
      )}
    </Card>
  );

  // Kanban View
  const KanbanView = () => (
    <div className="flex gap-4 overflow-x-auto pb-4">
      {stages.map((stage) => {
        const stageDeals = dealsByStage[stage.id] || [];
        const stageAmount = stageDeals.reduce((sum, d) => sum + d.amount, 0);

        return (
          <div key={stage.id} className="flex-shrink-0" style={{ width: 300 }}>
            <Card
              title={
                <div>
                  <div className="flex items-center gap-2">
                    <div
                      className="w-3 h-3 rounded-full"
                      style={{ backgroundColor: stage.color }}
                    />
                    <span>{stage.name}</span>
                    <Tag>{stageDeals.length}</Tag>
                  </div>
                  <div className="text-sm font-normal text-gray-500 mt-1">
                    â‚º{stageAmount.toLocaleString('tr-TR')}
                  </div>
                </div>
              }
              className="h-full"
              bodyStyle={{ padding: '12px', maxHeight: '600px', overflowY: 'auto' }}
            >
              {stageDeals.map((deal) => (
                <DealCard key={deal.id} deal={deal} />
              ))}
              {stageDeals.length === 0 && (
                <div className="text-center text-gray-400 py-4">FÄ±rsat yok</div>
              )}
            </Card>
          </div>
        );
      })}
    </div>
  );

  // List View
  const ListView = () => (
    <Card>
      <div className="space-y-3">
        {filteredDeals.map((deal) => (
          <Card key={deal.id} className="cursor-pointer hover:shadow-md" onClick={() => handleEdit(deal)}>
            <div className="flex justify-between items-start">
              <div className="flex-1">
                <div className="flex items-center gap-3 mb-2">
                  <Text strong className="text-lg">
                    {deal.title}
                  </Text>
                  <Tag color={stages.find((s) => s.id === deal.stageId)?.color}>
                    {stages.find((s) => s.id === deal.stageId)?.name}
                  </Tag>
                  <Tag color={statusColors[deal.status]}>{deal.status}</Tag>
                </div>
                {deal.description && <Text type="secondary">{deal.description}</Text>}
              </div>
              <div className="text-right">
                <div className="text-xl font-semibold text-green-600">
                  â‚º{deal.amount.toLocaleString('tr-TR')}
                </div>
                <div className="text-sm text-gray-500">OlasÄ±lÄ±k: {deal.probability}%</div>
                {deal.expectedCloseDate && (
                  <div className="text-sm text-gray-500">
                    {dayjs(deal.expectedCloseDate).format('DD/MM/YYYY')}
                  </div>
                )}
              </div>
            </div>
          </Card>
        ))}
        {filteredDeals.length === 0 && (
          <div className="text-center text-gray-400 py-8">FÄ±rsat bulunamadÄ±</div>
        )}
      </div>
    </Card>
  );

  return (
    <div className="p-6">
      <Row gutter={[16, 16]} className="mb-6">
        <Col span={24}>
          <div className="flex justify-between items-center">
            <Title level={2} className="!mb-0">
              FÄ±rsatlar
            </Title>
            <Space>
              <Button icon={<ReloadOutlined />} onClick={() => refetch()} loading={isLoading}>
                Yenile
              </Button>
              <Button.Group>
                <Button
                  type={viewMode === 'kanban' ? 'primary' : 'default'}
                  icon={<AppstoreOutlined />}
                  onClick={() => setViewMode('kanban')}
                >
                  Kanban
                </Button>
                <Button
                  type={viewMode === 'list' ? 'primary' : 'default'}
                  icon={<UnorderedListOutlined />}
                  onClick={() => setViewMode('list')}
                >
                  Liste
                </Button>
              </Button.Group>
              <Button type="primary" icon={<PlusOutlined />} onClick={handleCreate}>
                Yeni FÄ±rsat
              </Button>
            </Space>
          </div>
        </Col>
      </Row>

      <div className="mb-6">
        <DealsStats
          totalDeals={stats.total}
          totalAmount={stats.totalAmount}
          wonDeals={stats.won}
          wonAmount={stats.wonAmount}
          loading={isLoading}
        />
      </div>

      <Row gutter={[16, 16]} className="mb-6">
        <Col flex="auto">
          <Input
            placeholder="FÄ±rsat ara..."
            prefix={<SearchOutlined />}
            value={searchText}
            onChange={(e) => setSearchText(e.target.value)}
            size="large"
            allowClear
          />
        </Col>
      </Row>

      {viewMode === 'kanban' ? <KanbanView /> : <ListView />}

      {/* Create/Edit Modal */}
      <DealModal
        open={modalOpen}
        deal={selectedDeal}
        loading={createDeal.isPending || updateDeal.isPending}
        stages={stages}
        onCancel={() => setModalOpen(false)}
        onSubmit={handleSubmit}
        onDelete={handleDelete}
      />
    </div>
  );
}
